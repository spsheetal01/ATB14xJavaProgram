package ex_03_Operators;

public class Lab0027_Relational_Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        boolean c = a > b;
        System.out.println(c);

        int X_age = 15;
        int Y_age = 5;
        boolean z = X_age >= Y_age;
        System.out.println(z);
// ralational operator always give result in the Boolean form
    }
}
