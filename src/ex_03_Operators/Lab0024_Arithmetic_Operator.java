package ex_03_Operators;

public class Lab0024_Arithmetic_Operator {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        float c=3.2f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);
        System.out.println("a+b"); //string will be print
        System.out.println(a+b);
    }
}
